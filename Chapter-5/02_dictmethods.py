#Methods used in Dictionary

marks = {
    "Ram" : 100,
    "Shyam" : 94,
    "Hari" : 78
}

#Methods
# marks.items() : Returns a list of (key,value)tuples
# marks.keys() : Returns a list containing dictionary keys / Ram,Shyam are keys 
# marks.values() : Returns a list containing dictionary values / 100,94 are values 
# marks.update({"friends:"}) : Updates the dictionary with supplied key values
# marks.get("name"): Returns the value of the specified keys